    private void abrirTelaVenda(int idCliente, String nome, String curso, String cpf) throws SQLException {

        JTextField campoValor = new JTextField(10);

        JPanel painel = new JPanel();
        painel.setLayout(new GridLayout(4, 2, 5, 5));
        painel.add(new JLabel("CPF:"));
        painel.add(new JLabel(cpf));
        painel.add(new JLabel("Nome:"));
        painel.add(new JLabel(nome));
        painel.add(new JLabel("Curso:"));
        painel.add(new JLabel(curso));
        painel.add(new JLabel("Valor da venda:"));
        painel.add(campoValor);

        int resultado = JOptionPane.showConfirmDialog(this, painel, "Registrar Venda", JOptionPane.OK_CANCEL_OPTION, JOptionPane.PLAIN_MESSAGE);

        if (resultado == JOptionPane.OK_OPTION) {

            if (campoValor.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Informe o valor");
                return;
            }

            try {
                double valor = Double.parseDouble(campoValor.getText());

                VendasDAO vendasDAO = new VendasDAO();

                String dataAtual = new java.text.SimpleDateFormat("yyyy-MM-dd").format(new java.util.Date());

                int res = vendasDAO.inserirVenda(dataAtual, valor, idCliente);

                if (res == 1) {
                    JOptionPane.showMessageDialog(this, "Venda registrada com sucesso!");
                } else {
                    JOptionPane.showMessageDialog(this, "Erro ao registrar venda");
                }

            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Valor inválido");
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, "Erro no banco de dados");
            }
        }
    }
