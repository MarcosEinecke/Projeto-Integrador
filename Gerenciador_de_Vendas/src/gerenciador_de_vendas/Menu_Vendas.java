    private void vConsultarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_vConsultarButtonActionPerformed

        String cpf;
        cpf = vNomeTextField2.getText();

        
        if (cpf.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Digite um CPF");
            return;
        }

        try {
            ClientesDAO clientesDAO = new ClientesDAO();
            ResultSet rs = clientesDAO.consultarCPF(cpf);

            if (!rs.next()) {
                JOptionPane.showMessageDialog(this, "CPF não encontrado");
                return;
            }

            int idCliente = rs.getInt("id");
            String nome = rs.getString("nome");
            String curso = rs.getString("curso");

            abrirTelaVenda(idCliente, nome, curso, cpf);

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Erro ao consultar CPF");
        }
    }//GEN-LAST:event_vConsultarButtonActionPerformed

