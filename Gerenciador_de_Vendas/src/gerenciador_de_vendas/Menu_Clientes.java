    private void cConsultarButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cConsultarButton1ActionPerformed
        String cpf = cNomeTextField.getText();

        if (cpf.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Digite um CPF");
            return;
        }

        try {
            ResultSet rs = clientesDAO.consultarCPF(cpf);

            DefaultTableModel model = (DefaultTableModel) Tabela_Clientes.getModel();
            model.setRowCount(0);

            boolean encontrou = false;

            while (rs.next()) {
                encontrou = true;
                model.addRow(new Object[]{rs.getInt("id"), rs.getString("nome"), rs.getString("curso"), rs.getString("cpf"), rs.getDouble("saldo")});
            }

            if (!encontrou) {
                JOptionPane.showMessageDialog(this, "CPF não encontrado");
            }

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Erro ao consultar CPF");
        }
    }//GEN-LAST:event_cConsultarButton1ActionPerformed

