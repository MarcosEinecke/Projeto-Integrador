    private void loadClientesData() throws SQLException {
        ClientesDAO clientesDao = new ClientesDAO();
        List<Clientes> clientes = clientesDao.getClientes();
        String[][] data = new String[clientes.size()][5];

        for (int i = 0; i < clientes.size(); i++) {
            data[i][0] = String.valueOf(clientes.get(i).getCid());
            data[i][1] = clientes.get(i).getNome();
            data[i][2] = clientes.get(i).getCurso();
            data[i][3] = clientes.get(i).getCpf();
            data[i][4] = String.valueOf(clientes.get(i).getSaldo());
            System.out.println(clientes.get(i).getNome());
        }
        String[] nomeColuna = {"Id", "Nome", "Curso", "CPF", "Saldo"};
        Tabela_Clientes.setModel(new javax.swing.table.DefaultTableModel(data, nomeColuna));
    }
