    private void cCriarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cCriarButtonActionPerformed

        JTextField campoCpf = new JTextField(15);
        JTextField campoNome = new JTextField(15);
        JTextField campoCurso = new JTextField(15);

        JPanel painel = new JPanel();
        painel.setLayout(new GridLayout(3, 3, 5, 5));

        painel.add(new JLabel("CPF: "));
        painel.add(campoCpf);
        painel.add(new JLabel("Nome:"));
        painel.add(campoNome);
        painel.add(new JLabel("Curso:"));
        painel.add(campoCurso);

        int resultado = JOptionPane.showConfirmDialog(
                null,
                painel,
                "Cadastrar Cliente",
                JOptionPane.OK_CANCEL_OPTION,
                JOptionPane.PLAIN_MESSAGE
        );

        if (resultado == JOptionPane.OK_OPTION) {

            String nome = campoNome.getText();
            String curso = campoCurso.getText();
            String cpf = campoCpf.getText();

            System.out.println("Nome: " + nome);
            System.out.println("Curso: " + curso);
            System.out.println("CPF: " + cpf);

            String addNome = nome;
            String addCurso = curso;
            String addCpf = cpf;
            double addSaldo = 0;

            if (addNome.isEmpty() || addCurso.isEmpty() || addCpf.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Por favor preencha todos os campos", "ERROR", JOptionPane.ERROR_MESSAGE);
            } else {
                Clientes novoClientes = new Clientes(0, addNome, addCurso, addCpf, addSaldo);
                ClientesDAO clientesDao;

                try {
                    clientesDao = new ClientesDAO();
                    int res = clientesDao.InserirClientes(novoClientes);
                    if (res == 1) {
                        JOptionPane.showMessageDialog(this, "Cliente cadastrado com sucesso", "SUCESSO", JOptionPane.INFORMATION_MESSAGE);
                        this.clearForm();
                        loadClientesData();
                    } else {
                        JOptionPane.showMessageDialog(this, "Erro ocorrido ao inserir o cliente", "ERROR", JOptionPane.ERROR_MESSAGE);
                    }
                } catch (SQLException e) {
                    JOptionPane.showMessageDialog(this, "Erro ocorrido ao inserir o cliente", "ERROR", JOptionPane.ERROR_MESSAGE);
                }

            }
        }


    }//GEN-LAST:event_cCriarButtonActionPerformed

    private void cPagarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cPagarButtonActionPerformed
        
        int linhaSelecionada = Tabela_Clientes.getSelectedRow();

        if (linhaSelecionada == -1) {
            JOptionPane.showMessageDialog(this, "Selecione um cliente na tabela");
            return;
        }

        int idCliente = Integer.parseInt(Tabela_Clientes.getValueAt(linhaSelecionada, 0).toString());

        int confirmacao = JOptionPane.showConfirmDialog(this, "Deseja quitar TODAS as vendas deste cliente?", "Confirmação de Pagamento", JOptionPane.YES_NO_OPTION);

        if (confirmacao != JOptionPane.YES_OPTION) {
            return;
        }

        try {
            VendasDAO vendasDAO = new VendasDAO();
            int totalApagadas = vendasDAO.pagarVendasPorCliente(idCliente);

            if (totalApagadas >= 0) {
                JOptionPane.showMessageDialog(this, "Pagamento realizado com sucesso!\nVendas quitadas: " + totalApagadas);

                loadClientesData();
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao realizar pagamento");
            }

        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Erro de conexão com o banco");
        }
    }//GEN-LAST:event_cPagarButtonActionPerformed

